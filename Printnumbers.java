package programmsJava;

public class Printnumbers {

	public static void main(String[] args) {
		
		//used for loop
		
		for(int i=1;i<10;i++) {
			System.out.print(i);
		}

		System.out.println("   for loop                 ");
		
		//while loop
		
		int x=1;
		while (x<10) {
			System.out.print(x);
			x++;
			
		}
		
		System.out.println(" while loop");
	}

}
